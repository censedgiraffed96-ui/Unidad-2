
package eva2_9_for;

public class EVA2_9_FOR {

    public static void main(String[] args) {
        //3 parametros para funcionar
        //punto inicial
        //donde va a acabar
        //como me voy a mover
        //i ----> indice (arreglo)
        //++ sumar uno, -- resta uno
        for(int f = 1; f <= 10; f++){
            System.out.println(f + "Hello Mi Once");
        }
        for(int f = 10; f >= 1; f--){
            System.out.println(f + "Hello Mi Once");
        }
    }
    
}
